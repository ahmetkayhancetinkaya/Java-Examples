package odev3Kodlama.Persons;

public class User {

	private int _id;
	private String _nationalIdentity;
	private String _firstName;
	private String _lastName;
	private String _adress;
	private String _telephone;
	private String _eMail;
	private String _password;



	

	public int get_id() {
		return _id;
	}

	public void set_id(int _id) {
		this._id = _id;
	}

	public String get_nationalIdentity() {
		return _nationalIdentity;
	}

	public void set_nationalIdentity(String _nationalIdentity) {
		this._nationalIdentity = _nationalIdentity;
	}

	public String get_firstName() {
		return _firstName;
	}

	public void set_firstName(String _firstName) {
		this._firstName = _firstName;
	}

	public String get_lastName() {
		return _lastName;
	}

	public void set_lastName(String _lastName) {
		this._lastName = _lastName;
	}

	public String get_adress() {
		return _adress;
	}

	public void set_adress(String _adress) {
		this._adress = _adress;
	}

	public String get_telephone() {
		return _telephone;
	}

	public void set_telephone(String _telephone) {
		this._telephone = _telephone;
	}

	public String get_eMail() {
		return _eMail;
	}

	public void set_eMail(String _eMail) {
		this._eMail = _eMail;
	}

	public String get_password() {
		return _password;
	}

	public void set_password(String _password) {
		this._password = _password;
	}

}
